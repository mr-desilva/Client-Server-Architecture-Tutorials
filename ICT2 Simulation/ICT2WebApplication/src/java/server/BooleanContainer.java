public class BooleanContainer {
    Boolean value = null;

    public Boolean getValue() {
        return value;
    }

    public void setValue(Boolean value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "BooleanContainer{" + "value=" + value + '}';
    }
    
}
